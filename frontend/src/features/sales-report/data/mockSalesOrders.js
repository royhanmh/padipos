const createOrder = ({
  id,
  orderNumber,
  orderDate,
  cashierName,
  orderType,
  tableNumber = null,
  category,
  customerName,
  items,
  tax,
  amountPaid,
}) => {
  const subTotal = items.reduce(
    (total, item) => total + item.quantity * item.unitPrice,
    0,
  );
  const total = subTotal + tax;

  return {
    id,
    orderNumber,
    orderDate,
    cashierName,
    orderType,
    tableNumber,
    category,
    customerName,
    items,
    subTotal,
    tax,
    total,
    amountPaid,
    change: amountPaid - total,
  };
};

